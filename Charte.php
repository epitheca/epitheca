            <p  class="sous-titre">Mentions légales</p>
Epitheca.fr est un système de gestion de base de données naturalistes géré par <a href="https://framasphere.org/people/2027e100824e0132ae4e2a0000053625">Mathieu MONCOMBLE</a> . Il est basé sur le code du programme epitheca.
Epitheca.fr est une base de données stockée sur les serveurs OVH. (Graveline2). En accord avec le Règlement (UE) 2016/679 du Parlement européen et du Conseil du 27 avril 2016, relatif à la protection des personnes physiques à l'égard du traitement des données à caractère personnel et à la libre circulation de ces données       
(RGPD), vous consentez en utilisant se service à ce que vos NOM, PRENOM et ADRESSE DE COURRIEL puisse être accessibles à Mathieu MONCOMBLE dans le cadre de l'utilisation du service epitheca.fr en fonction de votre autorisation.
Votre mot de passe est transféré via https (LETSENCRYPT) et stocké crypté par la fonction de hachage crytographique <a href="https://fr.wikipedia.org/wiki/MD5">MD5</a>.
Vos données (à l'exception de votre mot de passe) sont accessibles en clair par l'administrateur de la base de données. Il se refuse à les utiliser à l'exception de la réalisation de statistiques anonymes visant à l'amalioration du service.
epitheca utilise un cookie pour gérer votre session.

  <p  class="sous-titre">Pourquoi gérer ses données naturalistes via epitheca.fr ?</p>
 Actuellement il existe de nombreuses bases de données naturalistes. Certaines sont gérées par des associations, d'autres par des particuliers. Toutes utilisent des formats de stockage de données qui les rendent difficilement interopérables.
 En fonction de la région, voire des taxons, les naturalistes sont actuellement amenés à utiliser de nombreux systèmes différents.<br>
 Epitheca, fait le pari de l'utilisation de standards simples en reconnaissant l'important travail mené par l'<a href="https://inpn.mnhn.fr/accueil/index">INPN</a>:<br>
 <ul>
 <li>Le référentiel habitat est le <a href="https://inpn.mnhn.fr/habitat/cd_typo/22">HABREF</a></li>
  <li>Le référentiel taxonomique est le <a href="https://inpn.mnhn.fr/telechargement/referentielEspece/referentielTaxo">TAXREF</a></li>
  </ul>
<br>Ainsi en utilisant epitheca.fr vous pouvez fournir vos données à qui vous le souhaitez en exportant simplement un fichier tableur depuis la base.
<br>epitheca.fr étant basé sur le programme libre epitheca, vous pouvez le stocker sur votre propre serveur, créer et utiliser se système pour votre association naturaliste et transférer facilement des données d'une instance à une autre. 
